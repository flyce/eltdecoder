function protocol(protocolFlag) {
    return protocolFlag === '1' ? "user" : "location";
}

function serialType(serialTypeFlag) {
    let serialType;
    switch (serialTypeFlag) {
        case "000": serialType = "ELT serial number"; break;
        case "001": serialType = "Aircraft operator and a serial number"; break;
        case "011": serialType = "24-bit aircraft address"; break;
        default: serialType = "Error code";
    }
    return serialType;
}

function certificate(certificateFlag) {
    return certificateFlag === "1" ? "YES" : "NO";
}

function AuxiliaryRadioLocatingDevice(AuxiliaryRadioLocatingDeviceFlag) {
    let AuxiliaryRadioLocatingDevice;
    switch (AuxiliaryRadioLocatingDeviceFlag) {
        case "00": AuxiliaryRadioLocatingDevice = "no auxiliary radio-locating device"; break;
        case "01": AuxiliaryRadioLocatingDevice = "121.5Mhz"; break;
        case "11": AuxiliaryRadioLocatingDevice = "other auxiliary radio-locating device"; break;
        default: AuxiliaryRadioLocatingDevice = "Error code"
    }
    return AuxiliaryRadioLocatingDevice;
}

function userType(userTypeFlag) {
    return userTypeFlag === "011" ? "serial user" : "Aviation User";
}

