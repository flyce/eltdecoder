function getAircraftCode(AircraftRegistration) {
    const length = AircraftRegistration.length / 6 - 1;
    let aircraft = "";
    for(let index = 0; index < length; ++index) {
        aircraft = aircraft + AircraftIdentificationDecoder(AircraftRegistration.substr(index * 6 + 3, 6));
    }
    return aircraft.replace(/\s/, '');
}

